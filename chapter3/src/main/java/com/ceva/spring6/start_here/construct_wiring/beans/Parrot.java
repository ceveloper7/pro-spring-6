package com.ceva.spring6.start_here.construct_wiring.beans;

public class Parrot {
    private String name;

    public Parrot(String name) {
        this.name = name;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Parrot{" +
                "name='" + name + '\'' +
                '}';
    }
}
